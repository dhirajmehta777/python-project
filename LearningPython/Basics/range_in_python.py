print(list(range(10)))#prints 0 to 9 digits
print("##########################")
print(list(range(5,10)))#prints 5 to 9 digits
print("##########################")
print(list(range(1,10,2)))#prints odd numbers
print("##########################")
print(list(range(0,10,2)))#prints even numbers
print("##########################")
print(list(range(10,1,-1)))#decrement, decending order i.e. 10 to 1
print("##########################")
print(list(range(-10,-5)))#negatives
print("##########################")
print(list(range(-10,-5,2)))#Negatives increment by 2