#print 1 to 10 numbers using for loop
for i in range(10):
    print(i)
#print even numbers between 1 to 10
for i in range(2,10,2):#(starting value, max value, increment/decrement vale)
    print(i)
#print odd numbers between 1 to 10
for i in range(1,10,2):#(starting value, max value, increment/decrement vale)
    print(i)
#print numbers in decending order
for i in range(10,1,-1):
    print(i)
